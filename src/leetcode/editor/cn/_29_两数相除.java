    public int divide(int a, int b) {
        long x = a, y = b;
        boolean isNeg = false;
        if ((x > 0 && y < 0) || (x < 0 && y > 0)) isNeg = true;
        if (x < 0) x = -x;
        if (y < 0) y = -y;
        long l = 0, r = x;
        while (l < r) {
            long mid = l + r + 1 >> 1;
            if (mul(mid, y) <= x) {
                l = mid;
            } else {
                r = mid - 1;
            }
        }
        long ans = isNeg ? -l : l;
        if (ans > Integer.MAX_VALUE || ans < Integer.MIN_VALUE) return Integer.MAX_VALUE;
        return (int)ans;
    }

    long mul(long a, long k) {
        long ans = 0;
        while (k > 0) {
            if ((k & 1) == 1) ans += a;
            k >>= 1;
            a += a;
        }
        return ans;
    }

    public int divide2(int dividend, int divisor) {
        if (dividend == Integer.MIN_VALUE && divisor == -1) {
            return Integer.MAX_VALUE;
        }

        boolean negative = (dividend < 0 && divisor > 0) || (dividend > 0 && divisor < 0);

        int result = 0;

        dividend = -Math.abs(dividend);
        divisor = -Math.abs(divisor);

        while (dividend <= divisor) {
            int multiDivisor = divisor;
            int r = 1;
            //while (dividend <= multiDivisor + multiDivisor)//è¿™æ ·åœ¨æ•°å€¼æ¯”è¾ƒå¤§çš„æƒ…å†µä¸‹ä¼šè¶Šç•Œ
            while (dividend - multiDivisor <= multiDivisor) {//é¿å…è¶Šç•Œ
                multiDivisor = multiDivisor << 1;
                r = r << 1;
            }
            dividend-=multiDivisor;
            result+=r;
        }

        return negative ? -result : result;
    }
